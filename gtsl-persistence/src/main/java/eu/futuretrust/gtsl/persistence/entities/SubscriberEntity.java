package eu.futuretrust.gtsl.persistence.entities;

import java.util.HashSet;
import java.util.Set;
import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.index.Indexed;
import org.springframework.data.mongodb.core.mapping.DBRef;
import org.springframework.data.mongodb.core.mapping.Document;

@Document(collection="subscribers")
public class SubscriberEntity {

  @Id
  private String id;

  @Indexed
  private String email;

  @DBRef
  private Set<TerritoryEntity> territories;

  public SubscriberEntity() {}

  public SubscriberEntity(String email) {
    this.email = email;
    this.territories = new HashSet<>();
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getEmail() {
    return email;
  }

  public void setEmail(String email) {
    this.email = email;
  }

  public Set<TerritoryEntity> getTerritories() {
    return territories;
  }

  public void setTerritories(
      Set<TerritoryEntity> territories) {
    this.territories = territories;
  }
}
